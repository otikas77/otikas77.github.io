document.querySelector(".cards_5").onmouseover = function(){
	document.querySelector(".your-title_1_5").style.display = "none";
	document.querySelector(".your-title_2_5").style.display = "none";
	document.querySelector(".bottom-part_5").style.display = "block";
}
document.querySelector(".cards_5").onmouseleave = function(){
	document.querySelector(".your-title_1_5").style.display = "block";
	document.querySelector(".your-title_2_5").style.display = "block";
	document.querySelector(".bottom-part_5").style.display = "none";
}	
 



